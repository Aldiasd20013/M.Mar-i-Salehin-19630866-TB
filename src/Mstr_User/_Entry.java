     private boolean ValidasiInput() {
        boolean result = false;
        JOptionPane pane = new JOptionPane();
        
        if (user_idField.getText().equals("")
                || namaField.getText().equals("")
                || jenis_kelaminCombo.getSelectedItem().equals("Pilih Jenis Kelamin")
                || tempat_lahirField.getText().equals("")
                || tanggal_lahirField.getText().equals("")
                || alamatField.getText().equals("")
                || passwordField.getText().equals("")){
            JDialog dialog = pane.createDialog(null,"Lengkapi Record Data");
            dialog.setAlwaysOnTop(true);
            dialog.setVisible(true);
            //JOptionPane.showMessageDialog(this, "Atribut tidak valid", "Informasi", 0);
            if ((user_idField.getText().equals(""))) {
                user_idField.requestFocus();
                String user_id = JOptionPane.showInputDialog("User ID?");
                user_idField.setText(user_id);
            }
            if ((namaField.getText().equals(""))) {
                namaField.requestFocus();
                String nama = JOptionPane.showInputDialog("Nama User?");
                namaField.setText(nama);
            }
            if ((jenis_kelaminCombo.getSelectedItem().equals("Pilih Jenis Kelamin"))) {
                jenis_kelaminCombo.requestFocus(); 
                String JKPane[] = {"Pilih Jenis Kelamin","Laki-laki","Perempuan"};
                JComboBox cb = new JComboBox(JKPane);
                int jenis_kelamin;
                jenis_kelamin = JOptionPane.showConfirmDialog(this,cb,"Jenis Kelamin User?",
                        JOptionPane.DEFAULT_OPTION);
                if(jenis_kelamin == JOptionPane.OK_OPTION) {
                    String pilihJK = (String)cb.getSelectedItem();
                    jenis_kelaminCombo.setSelectedItem(pilihJK);
                }
            }
            if ((tempat_lahirField.getText().equals(""))) {
                tempat_lahirField.requestFocus();
                String tempat_lahir = JOptionPane.showInputDialog("Tempat Lahir User?");
                tempat_lahirField.setText(tempat_lahir);
            }
            if ((tanggal_lahirField.getText().equals(""))) {
                tanggal_lahirField.requestFocus();
                String tanggal_lahir = JOptionPane.showInputDialog("Tanggal Lahir User? \nContoh Format : 2001-8-21(yyyy-M-d)");
                tanggal_lahirField.setText(tanggal_lahir);
            }
            if ((alamatField.getText().equals(""))) {
                alamatField.requestFocus();
                String alamat = JOptionPane.showInputDialog("Alamat User?");
                alamatField.setText(alamat);
            }
            if ((passwordField.getText().equals(""))) {
                passwordField.requestFocus();
                String password = JOptionPane.showInputDialog("Password User?");
                passwordField.setText(password);
            }
            result = false;
        } else {
            result = true;
        }
        return result;
    }
     
    void simpan() {
        if (ValidasiInput() == false) {
            return;
        } else {
            try {
                PD.Cari(user_idField.getText());
                PD.user_id = user_idField.getText();
                PD.nama = namaField.getText();
                PD.jenis_kelamin = jenis_kelaminCombo.getSelectedItem().toString();
                PD.tempat_lahir = tempat_lahirField.getText();
                PD.tanggal_lahir = tanggal_lahirField.getText();
                PD.alamat = alamatField.getText();
                PD.password = passwordField.getText();
                if (PD._Akses.equals("-")) {
                    user_idField.setEditable(false);
                    int pilih = JOptionPane.showConfirmDialog(this, "Record data : \n"
                            + user_idField.getText() + "\n"
                            + namaField.getText() + "\n"
                            + jenis_kelaminCombo.getSelectedItem()+ "\n"
                            + tempat_lahirField.getText() + "\n"
                            + tanggal_lahirField.getText() + "\n"
                            + alamatField.getText() + "\n"
                            + passwordField.getText() + "\n"
                            + "Akan terupdate ??", "NOTIFIKASI UPDATE RECORD", JOptionPane.YES_OPTION);
                    if (pilih == JOptionPane.YES_OPTION) {
                        PD.updatedata();
                        clearfield();
                    }
                } else {
                    user_idField.setEditable(false);
                    int pilih = JOptionPane.showConfirmDialog(this, "Record data : \n"
                            + user_idField.getText() + "\n"
                            + namaField.getText() + "\n"
                            + jenis_kelaminCombo.getSelectedItem()+ "\n"
                            + tempat_lahirField.getText() + "\n"
                            + tanggal_lahirField.getText() + "\n"
                            + alamatField.getText() + "\n"
                            + passwordField.getText() + "\n"
                            + "Akan ditambahkan ke database USER??", "NOTIFIKASI SIMPAN RECORD", JOptionPane.YES_OPTION);
                    if(pilih == JOptionPane.YES_OPTION) {
                        PD.simpandata();
                        clearfield();
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Kesalahan " + e);
            }
        }
    }
    
