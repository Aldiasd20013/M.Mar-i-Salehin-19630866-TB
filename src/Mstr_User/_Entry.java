    void autono() {
        try {
            Connection _Cnn = logic_koneksi.getConnection();
            String sql = "select max(right(user_id,2)) AS idAuto from mstr_user";
            Statement st = _Cnn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                if (rs.first() == false) {
                    user_idField.setText("User-01");
                } else {
                    rs.last();
                    int auto_f = rs.getInt(1) + 1;
                    String no = String.valueOf(auto_f);
                    int NoLong = no.length();
                    //jumlah 0
                    for (int a = 0; a < 2 - NoLong; a++) {
                        no = "0" + no;
                    }
                    user_idField.setText("User-" + no);
                }
            }
            rs.close();
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: \n" + e.toString(), "Kesalahan", JOptionPane.WARNING_MESSAGE);
        }
    }
    
