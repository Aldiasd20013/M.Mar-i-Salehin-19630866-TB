    private void namaFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_namaFieldKeyTyped
       char c = evt.getKeyChar();
       if (!((Character.isLetter(c)
             ||  (c == KeyEvent.VK_BACK_SPACE)
             ||  (c == KeyEvent.VK_DELETE)
             ||  (c == KeyEvent.VK_SPACE)
             ||  (c == KeyEvent.VK_ENTER)))) {
           getToolkit().beep();
           JOptionPane.showInternalMessageDialog(null, "Masukkan Huruf A-Z");
           evt.consume();
       }
       if (c == evt.VK_ENTER) {
           alamatField.requestFocus();
       }
        // TODO add your handling code here:
        
    }//GEN-LAST:event_namaFieldKeyTyped

