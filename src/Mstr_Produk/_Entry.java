void autono () {
    try { 
        Connection _Cnn = logic_koneksi.getConnection();
        String sql = "select max(right(prd_id,5)) AS IdAuto from mstr_produk";
        Statement st = _Cnn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        while (rs.next()) {
            if (rs.first() == false) {
                idpengunjungField.setText("P01");
            } else {
              rs.last();
              int auto_f = rs.getInt(1) + 1;
              String no = String.valueOf(auto_f);
              int NoLong = no.length();
              for (int a = 0; a < 6 - NoLong; a++) {
                  no = "0" + no;
              }
              idpengunjungField.setText("P0" + no);
            }
        }
        rs.close();
        st.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "ERROR: \n" + e.toString(), "Kesalahan", JOptionPane.WARNING_MESSAGE);
    }
}

