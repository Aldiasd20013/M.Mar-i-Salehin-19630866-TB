private boolean ValidasiInput() {
    boolean result = false;
    JOptionPane pane = new JOptionPane();
    
    if (idpengunjungField.getText().equals("")
            || namaField.getText().equals("")
            || alamatField.getText().equals("")
            || umurField.getText().equals("")
            || nohpField.getText().equals("")) {
        JDialog dialog = pane.createDialog(null,"Lengkapi Record Data");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        JOptionPane.showMessageDialog(this, "Atribut tidak valid", "Informasi", 0);//Awalnya Komentar
        if ((idpengunjungField.getText().equals(""))) {
           idpengunjungField.requestFocus();
           String id = JOptionPane.showInputDialog("Berapa id produk?");
           idpengunjungField.setText(id);
        }
        if ((namaField.getText().equals(""))) {
            namaField.requestFocus();
            String nama = JOptionPane.showInputDialog("Nama Produk?");
            namaField.setText(nama);
        }
        if ((alamatField.getText().equals(""))) {
            alamatField.requestFocus();
            String alamat = JOptionPane.showInputDialog("alamat?");
            alamatField.setText(alamat);
        }
        if ((umurField.getText().equals(""))) {
            umurField.requestFocus();
            String tp = JOptionPane.showInputDialog("umur?");
            umurField.setText(tp);
        }
        if ((nohpField.getText().equals(""))) {
            nohpField.requestFocus();
            String tp = JOptionPane.showInputDialog("No_HP?");
            nohpField.setText(tp);
        }
        result = false;
} else {
        result = true;
    }
    return result;
}
void simpan() {
    if (ValidasiInput() == false) {
        return;
    }else {
        try {
            PD.Cari(idpengunjungField.getText());
            PD.prd_id = idpengunjungField.getText();
            PD.nama = namaField.getText();
            PD.alamat = alamatField.getText();
            PD.umur = umurField.getText();
            PD.No_HP = nohpField.getText();
            if (PD._Akses.equals("-")) {
                idpengunjungField.setEditable(false);
                int pilih = JOptionPane.showConfirmDialog(this, "Record data : \n"
                        + idpengunjungField.getText() + "\n"
                        + namaField.getText() + "\n"
                        + alamatField.getText() + "\n"
                        + umurField.getText() + "\n"
                        + nohpField.getText() + "\n"
                        + "Akan terupdate ??", "NOTOFIKASI UPDATE RECORD", JOptionPane.YES_OPTION);
            if (pilih == JOptionPane.YES_OPTION) {
                PD.updatedata();
                clearField();
            }
        } else {
                idpengunjungField.setEditable(false);
                int pilih = JOptionPane.showConfirmDialog(this, "Record data : \n"
                        + idpengunjungField.getText() + "\n"
                        + namaField.getText() + "\n"
                        + alamatField.getText() + "\n"
                        + umurField.getText() + "\n"
                        + nohpField.getText() + "\n"
                        + "Akan ditambahkan ke databse PRODUK??", "NOTOFIKASI SIMPAN RECORD", JOptionPane.YES_OPTION);
                if (pilih == JOptionPane.YES_OPTION) {
                PD.simpandata();
                clearField();
            }
        }   
     } catch (Exception e) {
          JOptionPane.showMessageDialog(null, "Kesalahan " + e);    
        }
    }
}
